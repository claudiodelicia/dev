#solicitar os valores a serem calculados
x = int(input("diga o primeiro valor"))
y = int(input("diga o segundo valor"))



#exibir o resultado para o usuario
print(f"A soma de {x} + {y} é {x + y}")
print(f"A subtração de {x} - {y} é {x - y}")
print(f"A multiplicação de {x} x {y} é {x * y}")
print(f"A divisão de {x} / {y} é {x / y}")
print(f"O resto de {x} % {y} é {x % y}")
